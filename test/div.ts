import * as assert from 'assert'
import { RoundingOptions, parse, div, toString } from '../src/Big'
import { pipe } from 'fp-ts/lib/function'

function testDiv(
  x: string | number,
  y: string | number,
  expectedResult: string,
  ro: RoundingOptions
) {
  it(`Big.js test: ${x} / ${y} rounded to ${ro.dp} places using RoundingMode ${ro.rm}`, () => {
    assert.strictEqual(pipe(x, parse, div(parse(y), ro), toString), expectedResult)
  })
}

